/*************************************************************************
函数名：delay_ms
功能：不精确延时t ms
*************************************************************************/
void delay_ms(UINT32 t)
{
	SINT32 i;
	for( i=0;i<t;i++)
	{
		SINT32 a=42000;
		while(a--);
	}
}

/*************************************************************************
函数名：delay_us
功能：不精确延时t us
*************************************************************************/
void delay_us(UINT32 t)
{
	SINT32 i;
	for( i=0;i<t;i++)
	{
		SINT32 a=40;
		while(a--);
	}
}
